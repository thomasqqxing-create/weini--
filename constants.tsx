export const getRandomScenarios = (count: number): Scenario[] => {
  const pool = [...MOVIE_SCENARIOS_POOL];
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }
  return pool.slice(0, count);
};

